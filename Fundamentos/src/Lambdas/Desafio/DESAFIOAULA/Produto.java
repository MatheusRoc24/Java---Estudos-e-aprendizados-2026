package Lambdas.Desafio.DESAFIOAULA;

public class Produto {
    public String nome;
    private double preco;
    private double desconto;


    public Produto(){
        this.preco = preco;
        this.nome = nome;
        this.desconto = desconto;
    }

    public double getDesconto() {
        return desconto;
    }

    public String getNome() {
        return nome;
    }

    public double getPreco() {
        return preco;
    }
}
