    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    Movimento m = new Movimento();
    m.setTipo(cmbTipo.getSelectedItem().toString());
    int quantidade = Integer.parseInt (jsQuantidade.getValue().toString());
    m.setQuantidade(quantidade);
    m.setProduto((Produto)cmbProduto.getSelectedItem());
    
        try {
            this.movimentoDAO.salvar(m);
            JOptionPane.showMessageDialog(this, "Movimento lançado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

