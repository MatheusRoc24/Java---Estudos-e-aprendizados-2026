    private void preencherComboProdutos(){
    cmbProduto.removeAllItems();
    try {
        List<Produto> lista = produtoDAO.getProdutos();
        for(Produto p: lista) {
            cmbProduto.addItem(p); // agora é o objeto inteiro
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}

