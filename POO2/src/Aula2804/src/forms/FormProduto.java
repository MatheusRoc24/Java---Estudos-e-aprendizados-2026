    private void habilitarBtns(boolean salvar) {
        btnSalvar.setEnabled(salvar);
        btnNovo.setEnabled(!salvar);
        btnAtualizar.setEnabled(!salvar);
        btnExcluir.setEnabled(!salvar);
    }

    private void preencherTabela() {
        DefaultTableModel tabela = (DefaultTableModel) tblProdutos.getModel();
        tabela.setNumRows(0);
        try {
            List<Produto> lista = produtoDAO.getProdutos();
            for (Produto p : lista) {
                Object[] novaLinha = new Object[]{
                    p.getId(),
                    p.getNomeproduto(),
                    p.getValor()
                };
                    tabela.addRow(novaLinha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }

