    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Produto p = new Produto();
        p.setNomeproduto(txtNomeProduto.getText());
        p.setValor(Integer.parseInt(txtValor.getText()));

        try {
            this.produtoDAO.salvar(p);
            JOptionPane.showMessageDialog(this, "Produto salvo com sucesso");
            //limpar os campos
            limparCampos();
            preencherTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtPesquisar.getText());

        try {
            Produto p = this.produtoDAO.getProduto(id);
            txtId.setText(String.valueOf(p.getId()));
            txtNomeProduto.setText(p.getNomeproduto());
            txtValor.setText(String.valueOf(p.getValor()));
            habilitarBtns(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

